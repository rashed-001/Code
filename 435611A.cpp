ll gcd(ll a, ll b)
{
    if (!a)
        return b;
    return gcd(b % a, a);
}
bool isPrime(int n)
{
    if (n <= 1)
        return false;
    if (n <= 3)
        return true;
    if (n % 2 == 0 || n % 3 == 0)
        return false;
  
    for (int i = 5; i * i <= n; i = i + 6)
        if (n % i == 0 || n % (i + 2) == 0)
            return false;
  
    return true;
}
void solve()
{
    int n,a,b,s;
    cin>>n>>a>>b>>s;
    int mx=0,mn=0;
    for(int i=0;i<n-1;i++){
        mn+=a;
        mx+=b;
    }
    mn+=b;
    mx+=a;
    if(s>=mn && s<=mx)cout<<"YES"<<endl;
    else cout<<"NO"<<endl;

}
